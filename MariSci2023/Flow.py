# Dictionaries
# A python data structure that has a key - value pair
# Key - represents the identifier of its corresponding value

Dict1 = {
    4 : 'number4',
    5 : 'charity',
    6 : 'brain'
}

# Access dictionary item value
print(Dict1.get(4))
print(Dict1[4])

# Obtain Dictionary Keys
keys = Dict1.keys() # will return a list of dictionary keys

# Obtain Dictionary values
vals = Dict1.values() # will return a list of dictionary values

# Add New item
Dict1['Color'] = "Red" # We add a new Key "Color" with a value of "Red"


# Access Dictionary Key:Value pairs
pairs = Dict1.items()


# Change a key value pair
Dict1[4] = 'Not4'


# Remove a Dictionary key - value pair
Dict1.pop('5')



